-- ------------------------------------------------------------
-- SITE SETTINGS (singleton)
-- ------------------------------------------------------------

CREATE TABLE "site_settings" (
  "id"         integer PRIMARY KEY DEFAULT 1,
  "brand"      jsonb NOT NULL,
  "footer"     jsonb NOT NULL,
  "updated_at" timestamptz DEFAULT now(),
  CONSTRAINT "check_single_row" CHECK ("id" = 1)
