-- ============================================================
-- ROW LEVEL SECURITY (RLS)
-- ============================================================

-- redirects: Middleware dùng anon client → cần policy SELECT public
-- Nếu không có policy này, middleware sẽ không đọc được redirect rules
ALTER TABLE "redirects" ENABLE ROW LEVEL SECURITY;

CREATE POLICY "redirects_public_read"
ON "redirects"
FOR SELECT
USING (true);

-- Chỉ service role mới được INSERT/UPDATE/DELETE
CREATE POLICY "redirects_service_write"
ON "redirects"
FOR ALL
TO service_role
USING (true)
WITH CHECK (true);


