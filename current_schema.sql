-- ------------------------------------------------------------
-- PROFILES
-- ------------------------------------------------------------

CREATE TABLE "profiles" (
  "id"           uuid PRIMARY KEY,
  "username"     varchar(50)  UNIQUE NOT NULL,
  "display_name" varchar(100),
  "avatar_key"   text,
  "role"         varchar(20)  NOT NULL DEFAULT 'admin',
  "created_at"   timestamptz DEFAULT now(),
  "updated_at"   timestamptz DEFAULT now()
);

ALTER TABLE "profiles"
ADD CONSTRAINT "profiles_role_check"
CHECK ("role" IN ('admin', 'editor'));


-- ------------------------------------------------------------
-- CATEGORIES
-- ------------------------------------------------------------

CREATE TABLE "categories" (
  "id"          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "name"        varchar(100) NOT NULL,
  "slug"        varchar(100) NOT NULL,
  "priority"    integer DEFAULT 0,
  "status"      varchar(20)  DEFAULT 'active',
  "created_at"  timestamptz DEFAULT now(),
  "updated_at"  timestamptz DEFAULT now(),
  "deleted_at"  timestamptz
);

ALTER TABLE "categories"
ADD CONSTRAINT "categories_status_check"
CHECK ("status" IN ('active', 'inactive'));

-- Unique slug/name chỉ áp dụng cho row chưa bị soft delete
CREATE UNIQUE INDEX "categories_slug_unique_idx"
ON "categories" ("slug")
WHERE ("deleted_at" IS NULL);

CREATE UNIQUE INDEX "categories_name_unique_idx"
ON "categories" ("name")
WHERE ("deleted_at" IS NULL);


-- ------------------------------------------------------------
-- ARTICLES
-- ------------------------------------------------------------

CREATE TABLE "articles" (
  "id"              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "title"           varchar(255) NOT NULL,
  "slug"            varchar(255) NOT NULL,
  "summary"         text,
  "thumbnail_key"   text,
  "content"         jsonb        NOT NULL,
  "category_id"     bigint,
  "author_id"       uuid,
  "views"           bigint       NOT NULL DEFAULT 0,   -- tổng aggregate, flush từ Redis
  "status"          varchar(20)  NOT NULL DEFAULT 'draft',
  "featured"        boolean      DEFAULT false,
  "seo_title"       varchar(255),
  "seo_description" text,
  "search_vector"   tsvector,                          -- PostgreSQL Full-Text Search
  "created_at"      timestamptz  DEFAULT now(),
  "updated_at"      timestamptz  DEFAULT now(),
  "published_at"    timestamptz,
  "deleted_at"      timestamptz
);

ALTER TABLE "articles"
ADD CONSTRAINT "articles_status_check"
CHECK ("status" IN ('draft', 'published'));

-- Unique slug chỉ áp dụng cho row chưa bị soft delete
CREATE UNIQUE INDEX "articles_slug_unique_idx"
ON "articles" ("slug")
WHERE ("deleted_at" IS NULL);

-- Query indexes
CREATE INDEX "articles_category_id_idx"  ON "articles" ("category_id");
CREATE INDEX "articles_author_id_idx"    ON "articles" ("author_id");
CREATE INDEX "articles_status_idx"       ON "articles" ("status");
CREATE INDEX "articles_published_at_idx" ON "articles" ("published_at");
CREATE INDEX "articles_deleted_at_idx"   ON "articles" ("deleted_at");
